// Size of the serialized OrderCommand in bytes
// Header: 8 bytes
// command: 1 byte
// order_id: 8 bytes
// timestamp: 8 bytes
// user_id: 8 bytes
// market_id: 4 bytes
// price: 8 bytes
// size: 8 bytes
// side: 1 byte
// time_in_force: 1 byte
pub const ORDERCOMMANDSIZE: usize = 64;

/// This is the size of each frame sent or received via Aeron.
/// It is a nearest multiple of 32 bytes that can hold an OrderCommand.
pub const FRAMESIZE: i64 = 96;

const PACKED_U32_MASK: u64 = 0xFFFF_FFFF;
const PACKED_U16_MASK: u64 = 0xFFFF;

/// OrderCommand: OrderCommand Plays the central role throughout the processing of the Order.
/// It is created in the Gateway, and processed in VexCore in different processors through the Disruptor
#[derive(Debug, Clone)]
#[repr(C)]
pub struct OrderCommand {
    // --- Universal Fields (Relevant for all command types) ---
    /// The type of command this struct represents. Consumers **must** check this
    /// field first to determine how to process the rest of the data.
    pub command: OrderCommandType,

    /// Client Order ID
    pub client_order_id: u64,

    /// The primary identifier for the order. Its meaning depends on `command`.
    /// - For `PlaceOrder`: This is the **new**, unique ID generated by the gateway.
    /// - For `CancelOrder`: This is the ID of the **existing** order to be canceled.
    pub order_id: u64,

    /// The unique identifier of the user initiating the command.
    /// For 'PlaceOrder' : This is the actual user id
    /// For 'CancelOrder': this is set to the gateway id inside the OrderCommand Field
    /// This is required because, in the existing scenarior the gateway id(required to send
    /// back the response to gateway with gateway_id in the events handler) not present in
    /// the OrderCommand Field, so it is encoded in the order_id by the snowflake algorithm
    /// Everything works correctly untill the order_id is itself wrong that is sent the gateway
    /// and user_id is not essential for the CancelOrder hence it is the best choice to place
    /// gateway id to send response correctly.
    pub user_id: u64,

    /// The identifier for the market this command targets.
    pub market_id: u32,

    // --- `PlaceOrder`-Specific Fields ---
    // These fields are only valid if `command` is `PlaceOrder`. They are
    // undefined for `CancelOrder` and should be ignored by the processor.
    /// The price for the new order, represented as a fixed-point integer.
    /// **A special value is used to signify a market order.**
    /// - **Limit Order**: The specific price for the limit.
    /// - **Market Order**: `0` for a SELL, `u64::MAX` for a BUY. This convention
    ///   instructs the matching engine to trade at any available price.
    pub price: u64,

    /// The quantity of the base asset that is remaining to be filled/cancelled
    pub size: u64,

    /// The side of the order book for a new order.
    pub side: Side,

    /// The time-in-force policy for a new order.
    pub time_in_force: TimeInForce,

    /// Timestamp: Time at which the order hits the VexCore
    /// Recorded using Instant::now() for monotonically increasing time
    pub timestamp: u64,

    /// Status of the order
    pub status: Status,

    /// Passive order linked list
    pub events: Option<Box<MatcherTradeEvent>>,

    /// Final balance of the user/maker after this trade
    /// For Deposit/Withdraw commands, only balance[0] is used to represent the asset balance change
    /// For OrderBook commands, balance[0] is base currency, balance[1] is quote currency
    pub balance: [UserBalance; 2], // [0] = base currency, [1] = quote currency

    /// L2 Market Data Snapshot after the order is processed, it is not recorded when the status is Rejected
    pub l2_data: Option<L2MarketData>,
    /// Internal-only routing field: gateway that sent THIS command (not serialized)
    pub route_gateway_id: u8,
    /// Original order size before any fills (set from book order during cancel)
    pub original_size: u64,
}

